weather = 'snowy'

case weather
when 'sunny'
    puts "Get out your shades & SPF!"
when 'rainy'
    puts "Welcome to Raincouver, don't forget that bumpershoot!"
when 'cloudy'
    puts "Well, another grey day..."
when 'snowy'
    puts "Get ready for the city to come to a halt - it's snowing!"
end